s = 0

for n in range(1, 101):
    s = s + n

print(f"Resultado: {s}")


# Exercício simples para demonstrar:
# - utilização de laços de repetição;
# - funcionamento do for;
# - utilização do range();
# - operações matemáticas;
# - acumuladores;
# - e exibição de resultados com print().
#
# Observação:
# A variável "soma" funciona
# como um acumulador.
#
# A cada repetição do for,
# um novo número é adicionado
# ao valor atual da soma.
#
# O range(1, 101) percorre
# os números de 1 até 100.
#
# O número 101 não é incluído.