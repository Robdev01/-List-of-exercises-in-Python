n1 = int(input("Informe um número: "))
n2 = int(input("Informe outro número: "))

if n1 > n2:
    print(f"O maior número é {n1}")

elif n2 > n1:
    print(f"O maior número é {n2}")

else:
    print("Os dois números são iguais")


