n = int(input("Informe um número: "))

for m in range(1, 11):

 
    resultado = n * m

    print(f"{n} x {m} = {resultado}")


# Exercício simples para demonstrar:
# - entrada de dados com input();
# - conversão de valores com int();
# - utilização de laços de repetição;
# - funcionamento do for;
# - utilização do range();
# - operações matemáticas;
# - multiplicação;
# - e exibição de resultados com print().
#
# Observação:
# O range(1, 11) percorre
# os números de 1 até 10.
#
# A cada repetição,
# o número informado pelo usuário
# é multiplicado pelo valor atual do loop.